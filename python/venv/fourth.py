# for 반복문

my_list = [2, 3, 5, 7, 11]

for number in my_list:
    print(number)

# range 함수

# for i in renge(start, stop):
#   print(i)  start -> stop

for i in range(3, 11): # 3 ~ 10
    print(i)

for i in range(10):
    print(i) # 0 ~ 9

# range(start, stop, step)

for i in range(3, 17, 3):
    print(i)